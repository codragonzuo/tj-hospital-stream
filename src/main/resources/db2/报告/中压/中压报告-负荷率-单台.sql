    IEDNAME         VARGRAPHIC(100 CODEUNITS16),
    LOADFACTOR      DECIMAL(31, 4),
    CREATETIME      VARGRAPHIC(100 CODEUNITS16),
    PRIMARY KEY (RELOADQUARTERID)
);

